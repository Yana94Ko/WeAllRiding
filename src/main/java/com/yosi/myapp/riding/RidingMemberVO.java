package com.yosi.myapp.riding;

public class RidingMemberVO {
	private int ridingMemberNo;
	private int ridingNo;
	private String userId;
	private String nickname;
	private String gender;
	private int ridingCount;
	private int userScore;
	private int applyUser;
	
	
	public int getRidingMemberNo() {
		return ridingMemberNo;
	}
	public void setRidingMemberNo(int ridingMemberNo) {
		this.ridingMemberNo = ridingMemberNo;
	}
	public int getRidingNo() {
		return ridingNo;
	}
	public void setRidingNo(int ridingNo) {
		this.ridingNo = ridingNo;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public int getRidingCount() {
		return ridingCount;
	}
	public void setRidingCount(int ridingCount) {
		this.ridingCount = ridingCount;
	}
	public int getUserScore() {
		return userScore;
	}
	public void setUserScore(int userScore) {
		this.userScore = userScore;
	}
	public int getApplyUser() {
		return applyUser;
	}
	public void setApplyUser(int applyUser) {
		this.applyUser = applyUser;
	}
	
	
	
	
}
