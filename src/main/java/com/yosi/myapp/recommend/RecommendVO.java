package com.yosi.myapp.recommend;

public class RecommendVO {
	private String recNo;
	private String recTitle;
	private String recHashtag;
	private String recDescription;
	private int recLevel;
	private String recommendSendData;
	private String recWritedate;
	public String getRecNo() {
		return recNo;
	}
	public void setRecNo(String recNo) {
		this.recNo = recNo;
	}
	public String getRecTitle() {
		return recTitle;
	}
	public void setRecTitle(String recTitle) {
		this.recTitle = recTitle;
	}
	public String getRecHashtag() {
		return recHashtag;
	}
	public void setRecHashtag(String recHashtag) {
		this.recHashtag = recHashtag;
	}
	public String getRecDescription() {
		return recDescription;
	}
	public void setRecDescription(String recDescription) {
		this.recDescription = recDescription;
	}
	public int getRecLevel() {
		return recLevel;
	}
	public void setRecLevel(int recLevel) {
		this.recLevel = recLevel;
	}
	public String getRecommendSendData() {
		return recommendSendData;
	}
	public void setRecommendSendData(String recommendSendData) {
		this.recommendSendData = recommendSendData;
	}
	public String getRecWritedate() {
		return recWritedate;
	}
	public void setRecWritedate(String recWritedate) {
		this.recWritedate = recWritedate;
	}
	
}
