package com.yosi.myapp.comty;

public class ComtyVO {
	private int comtyNo;
	private String nickname;
	private String comtySubject;
	private String comtyWriteDate;
	private String comtyContent;
	private int comtyHit;	

	public int getComtyNo() {
		return comtyNo;
	}
	public void setComtyNo(int comtyNo) {
		this.comtyNo = comtyNo;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getComtySubject() {
		return comtySubject;
	}
	public void setComtySubject(String comtySubject) {
		this.comtySubject = comtySubject;
	}
	public String getComtyWriteDate() {
		return comtyWriteDate;
	}
	public void setComtyWriteDate(String comtyWriteDate) {
		this.comtyWriteDate = comtyWriteDate;
	}
	public String getComtyContent() {
		return comtyContent;
	}
	public void setComtyContent(String comtyContent) {
		this.comtyContent = comtyContent;
	}
	public int getComtyHit() {
		return comtyHit;
	}
	public void setComtyHit(int comtyHit) {
		this.comtyHit = comtyHit;
	}
	
}